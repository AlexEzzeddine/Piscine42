char	*ft_rot42(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] >= 'a' && s[i] <= 'z')
			s[i] = ((s[i] - 97) + 42) % 26 + 97;
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = ((s[i] - 65) + 42) % 26 + 65;
		i++;
	}
	return (s);
}
