#include "ft_list.h"

int		ft_list_size(t_list *begin_list)
{
	int		i;

	i = 0;
	if (!begin_list)
	{
		return (0);
	}
	while (begin_list->next != 0)
	{
		i++;
		begin_list = begin_list->next;
	}
	return (i + 1);
}
